// A string literal in Java is an exact sequence of characters enclosed in quotation marks
public class StringLiterals {
	
	public static void main(String[] args) {
		
		// Java will display WHATEVER is within the quotations
		System.out.println("This is a string literal.");
		System.out.println("ABC123#$%abc");
		System.out.println("Have a GREAT day!!");
		System.out.println("AP CS A Rocks!!!");
		System.out.println("Missspelld Word");
		
	}
	
}
