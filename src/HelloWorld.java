// Class Declaration: Identifies the name, start, and end of the class
public class HelloWorld {

    // main Method: controls all of the action in the program
    public static void main(String[] args) {

        // System.out: object that generates output to the console
        // System.out.print: displays the arguments on screen. does not print \n char
        System.out.print("AP ");
        System.out.print("CS ");
        System.out.print("A ");
        // System.out.println: displays the arguments on screen. prints \n char
        System.out.println("Rocks!");
        System.out.println("Hello World");

    }

}